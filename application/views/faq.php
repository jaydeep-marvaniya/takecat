        <h5><b>What is Computerized Adaptive Test?</b></h5>
        <p style="line-height:200%;">
          Computerized adaptive testing (CAT) is a form of computer-based test that adapts to the examinee's ability level. Based on student responses, the computer program adjusts the difficulty of questions throughout the assessment. For example, a student who answers a question correctly will receive a more challenging item, while an incorrect answer generates an easier question. 
        </p>
        <br>
        <h5><b>How Computerized Adaptive Test works?</b></h5>
        <p style="line-height:200%;">
          The basic computer-adaptive testing method is an <a href="http://en.wikipedia.org/wiki/Iteration">iterative algorithm</a> with the following steps:<br>
          <img src="<?php echo $this->config->base_url(); ?>assets/Images/bullet.jpg"/>The pool of available items is searched for the optimal item, based on the current estimate of the examinee's ability<br>
          <img src="<?php echo $this->config->base_url(); ?>assets/Images/bullet.jpg"/>The chosen item is presented to the examinee, who then answers it correctly or incorrectly<br>
          <img src="<?php echo $this->config->base_url(); ?>assets/Images/bullet.jpg"/>The ability estimate is updated, based upon all prior answers<br>
          <img src="<?php echo $this->config->base_url(); ?>assets/Images/bullet.jpg"/>Steps 1–3 are repeated until a termination criterion is met
        </p>
